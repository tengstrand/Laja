package net.sf.laja.launch;

public class InargumentException extends Exception {

	public InargumentException() {
	}

	public InargumentException(String message) {
		super(message);
	}

	public InargumentException(Throwable cause) {
		super(cause);
	}

	public InargumentException(String message, Throwable cause) {
		super(message, cause);
	}
}
